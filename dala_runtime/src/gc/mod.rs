/// GC statistics for a collection cycle.
#[derive(Debug, Clone, Default)]
pub struct GCStats {
    pub heap_words_before: usize,
    pub heap_words_after: usize,
    pub stack_words: usize,
    pub roots_scanned: usize,
    pub objects_copied: usize,
    pub time_ns: u64,
}

/// GC configuration.
#[derive(Debug, Clone)]
pub struct GCConfig {
    /// Initial nursery size (in words)
    pub nursery_size: usize,
    /// Tenure threshold - objects surviving this many collections are promoted
    pub max_copy: usize,
    /// Full collection threshold (words allocated before triggering full GC)
    pub fullsweep_after: usize,
}

impl Default for GCConfig {
    fn default() -> Self {
        Self {
            nursery_size: 233,
            max_copy: 7,
            fullsweep_after: 65536,
        }
    }
}

/// Perform a garbage collection for the given process.
///
/// This is called when the process needs more heap space or when
/// reductions are exhausted. The GC:
///
/// 1. Scans the root set (stack, registers, mailbox)
/// 2. Copies live objects to a new heap (semi-space copying)
/// 3. Updates all pointers
/// 4. Frees the old heap
///
/// # Safety
///
/// This function must be called at a safepoint - all live references
/// must be visible on the stack or in the root set. The compiler
/// generates stack maps that describe which slots contain pointers.
pub unsafe fn collect(process: &mut Process, need_words: usize) -> Result<(), &'static str> {
    let _stats = GCStats::default();
    log::trace!(
        "GC triggered for process {}: heap_ptr={:?}, need={} words",
        process.pid,
        process.heap_ptr,
        need_words
    );

    // 1. Build root set first (needs &mut process)
    let rootset = RootSet::from_process(process);

    // 2. Calculate live size from root set + heap
    let live_size = (process.heap_ptr as usize) - (process.heap_start() as usize);
    let new_size = (live_size * 2).max(need_words).max(233);

    // 3. Perform copying collection (needs &mut process)
    let new_heap = copy_collection(process, new_size)?;

    // 4. Update root pointers after collection
    // (copy_collection already updates registers and stack in-place)

    // 4. Update process heap pointer
    process.heap_ptr = new_heap;

    log::trace!("GC complete for process {}", process.pid);

    Ok(())
}

/// Check if GC is needed and perform it if so.
///
/// Called by the AOT-compiled code before allocations.
/// Returns the updated heap pointer after potential GC.
pub unsafe fn maybe_collect(process: &mut Process, need_words: usize) -> *mut Term {
    let available = process.heap_top as usize - process.heap_ptr as usize;
    if available < need_words {
        collect(process, need_words).expect("GC failed");
    }
    let ptr = process.heap_ptr;
    process.heap_ptr = unsafe { process.heap_ptr.add(need_words) };
    ptr
}

