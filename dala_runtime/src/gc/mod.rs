/// Stack map entry - describes a single stack slot at a safepoint.
#[repr(C)]
#[derive(Debug, Clone, Copy)]
pub struct StackMapEntry {
    /// Offset from stack base (in words)
    pub offset: u32,
    /// Whether this slot contains a pointer (vs. immediate value)
    pub is_pointer: bool,
    /// If pointer, the type of value (for precise GC)
    pub value_type: StackMapType,
}

#[repr(u8)]
#[derive(Debug, Clone, Copy)]
pub enum StackMapType {
    Unknown,
    TuplePointer,
    ListPointer,
    BoxedPointer,
    FunPointer,
    MapPointer,
    BinaryPointer,
    MaybePointer, // could be either immediate or pointer
}

/// A stack map for a single safepoint in compiled code.
#[repr(C)]
pub struct StackMap {
    /// The instruction offset this map corresponds to
    pub instruction_offset: u32,
    /// Number of live entries
    pub num_entries: u32,
    /// The live stack slots
    pub entries: [StackMapEntry; 0], // Flexible array member
}

impl StackMap {
    /// Iterate over all entries in this stack map.
    pub fn entries(&self) -> &[StackMapEntry] {
        unsafe { std::slice::from_raw_parts(self.entries.as_ptr(), self.num_entries as usize) }
    }
}
