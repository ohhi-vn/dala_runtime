//! Garbage Collector - BEAM-style generational copying garbage collector.
//!
//! The BEAM GC uses a generational copying collection strategy:
//! - Young heap: nursery generation collected frequently
//! - Old heap: tenured generation collected less frequently
//!
//! The GC is triggered when a process exhausts its heap or after
//! a certain number of allocations. It is a stop-the-world collector
//! per-process (other processes continue running).
//!
//! Stack maps and root maps are critical for correctness:
//! - The compiler generates metadata describing which stack slots
//!   and registers contain heap pointers at each safepoint.
//! - This allows the GC to trace all live references accurately.

